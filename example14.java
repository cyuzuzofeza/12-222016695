public class example14 {
    public static void main(String[] args) {  
        //Declaring a variable for switch expression  
        int money=1000;  
        //Switch expression  
        switch(money){  
        //Case statements  
        case 500: System.out.println("500");  
        break;  
        case 1000: System.out.println("1000");  
        break;  
        case 2000: System.out.println("2000");  
        case 3000: System.out.println("3000");
        break;  
        //Default case statement  
        default:System.out.println("Not in 500, 1000,2000 or 3000");  
        }  
    }  
    }  

